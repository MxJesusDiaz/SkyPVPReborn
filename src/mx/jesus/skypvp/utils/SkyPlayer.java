package mx.jesus.skypvp.utils;

public class SkyPlayer {
	
	
	public int kills;
	public int deaths;
	public int killstreak;
	public int money;
	
	public SkyPlayer(int kills, int deaths, int killstreak, int money){
		this.kills = kills;
		this.deaths = deaths;
		this.killstreak = killstreak;
		this.money = money;
		
	}

}
